# Remove any tracked files that live under a node_modules directory
$files = git ls-files | Where-Object { $_ -match 'node_modules' }
if (-not $files) { Write-Host 'No tracked node_modules files found' ; exit 0 }
Write-Host ('Found ' + $files.Count + ' tracked node_modules files')
# Group by top-level node_modules dir to run git rm on directories where possible
$dirs = $files | ForEach-Object { $_ -replace '/.*$','' } | Select-Object -Unique
# Attempt to remove common node_modules directories
$commonDirs = @('backend/node_modules','foodbridge/client/node_modules','node_modules')
foreach ($d in $commonDirs) {
    if (Test-Path $d) {
        Write-Host "git rm -r --cached $d"
        git rm -r --cached $d
    }
}
# Fallback: remove individual files
foreach ($f in $files) {
    Write-Host ("git rm --cached --ignore-unmatch `"$f`"")
    git rm --cached --ignore-unmatch "$f"
}
Write-Host 'Done. Run git add .gitignore and commit.'
