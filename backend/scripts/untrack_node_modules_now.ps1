Set-Location 'D:\Food Waste Reduction Project'
$files = git ls-files
$nodeFiles = $files | Where-Object { $_ -match 'node_modules' }
if (-not $nodeFiles -or $nodeFiles.Count -eq 0) { Write-Host 'No tracked node_modules files found'; exit 0 }
Write-Host "Found $($nodeFiles.Count) tracked node_modules files"
$dirs = $nodeFiles | ForEach-Object { ($_ -replace '\\','/') -replace '/node_modules.*$','/node_modules' } | Select-Object -Unique
foreach ($d in $dirs) {
    Write-Host "Removing from index: $d"
    git rm -r --cached --ignore-unmatch $d
}
Write-Host 'Staging .gitignore'
git add .gitignore
Write-Host 'Untracking complete. Run `git status` to review changes. (No commit performed.)'